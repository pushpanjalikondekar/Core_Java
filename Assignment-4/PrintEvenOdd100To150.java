public class PrintEvenOdd100To150{
	public static void main(String[] args){
		
		System.out.println("Even Numbers from 100 to 150 are: ");
		for(int i = 100 ; i <= 150 ; i+=2 ){
			System.out.println(i);
		}

		System.out.println("Odd Numbers from 100 to 150 are: ");
		for(int i = 101 ; i <= 150 ; i+=2 ){
			System.out.println(i);
		}

	}
}